package chapter4;

import java.util.Scanner;

public class Comparator {
    private int number;

    public int checkIntegers(int firstNumber, int secondNumber) {
        if (firstNumber == secondNumber) return number = (0);
        if (firstNumber > secondNumber) return number = (1);
        if (secondNumber > firstNumber) return number = (-1);

        return number;

    }
}
