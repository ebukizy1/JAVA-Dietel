package easterTask;

public class TriangleNumbers {
    public static void main(String[] args) {

        int i;
        int k;
        int n = 6;
        for (i = 1; i <= n; i++) {
            for (k = i; k<=n; k++) {
                System.out.print(i);
            }
            System.out.println();
        }
    }
}
