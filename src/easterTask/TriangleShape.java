package easterTask;

import java.util.Scanner;

public class TriangleShape {
    public static void main(String[] args) {
        int i;
        int k;
        int n = 6;

        for (i = 1; i <= n; i++) {
            for (k = 1; k <= i; k++) {
                System.out.print(i);
            }
            System.out.println();
        }


    }
}